<script>  function m2() {
    var m2 = document.getElementById("m2");
    var m2_CHECK = document.getElementById("m2_CHECK");
 
    if (m2_CHECK.checked) {
        m2.style.display = "block";
        console.log(44)

        $("#m2 :input").attr("disabled", false);
    } else {
        m2.style.display = "none";
        console.log(5)
        $("#m2 :input").attr("disabled", true);
    }
}</script>
